package br.com.ultcode.colecoes.classes;

import java.util.Objects;

/**
 *
 * @author Geraldo
 */
public class Consumidor implements Comparable<Consumidor> {

    private String nome;
    private String cpf;

    public Consumidor(String nome, String cpf) {
        this.nome = nome;
        this.cpf = cpf;
    }

    @Override
    public int hashCode() {
        return cpf.hashCode();
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Consumidor other = (Consumidor) obj;
        if (!Objects.equals(this.cpf, other.cpf)) {
            return false;
        }
        return true;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    @Override
    public int compareTo(Consumidor o){
        return this.cpf.compareTo(o.getCpf());
    }

    @Override
    public String toString() {
        return "Consumidor{" + "nome=" + nome + ", cpf=" + cpf + '}';
    }

}
